package com.codingdojo.stefano.modelos;

public class OmikujiModel {
	private int numero;
	private String nombreCiudad;
	private String nombrePersona;
	private String profesion;
	private String serVivo;
	private String algoBueno;
	
	public OmikujiModel() {
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public String getNombreCiudad() {
		return nombreCiudad;
	}

	public void setNombreCiudad(String nombreCiudad) {
		this.nombreCiudad = nombreCiudad;
	}

	public String getNombrePersona() {
		return nombrePersona;
	}

	public void setNombrePersona(String nombrePersona) {
		this.nombrePersona = nombrePersona;
	}

	public String getProfesion() {
		return profesion;
	}

	public void setProfesion(String profesion) {
		this.profesion = profesion;
	}

	public String getSerVivo() {
		return serVivo;
	}

	public void setSerVivo(String serVivo) {
		this.serVivo = serVivo;
	}

	public String getAlgoBueno() {
		return algoBueno;
	}

	public void setAlgoBueno(String algoBueno) {
		this.algoBueno = algoBueno;
	}	
}
